def solution(n):
    answer = 0
    for i in range(2, n+1):
        if i == 2 or i == 3:
            answer += 1
            continue
        for j in range(2, int(i ** 0.5) + 1):
            if i % j == 0:
                break
            if j == int(i ** 0.5):
                answer += 1
    return answer


print(solution(10))  # 4
print(solution(5))  # 3

